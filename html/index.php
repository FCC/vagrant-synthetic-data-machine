<h2>About this Repo?</h2>

<p>This repo is maintained at <a href="https://github.com/fccdata/vagrant-synthetic-data-machine">github.com/fccdata/vagrant-synthetic-data-machine</a>.</p>

<h2>What?</h2>

This will be a vagrant virtual machine you can download and create synthetic data using 
Ben Keen's simple and most excellent web-based <a href="https://github.com/benkeen/generatedata">generatedata v3 (alpha)</a>.

See the demo at <a href="http://alpha.generatedata.com">alpha.generatedata.com</a>

<h2>Why?</h2>

Because it should be easy for economist, policymaker, or developer in Government to have fake data to test and use before collecting real data. Having synthetic data that does not represent real people or organizations makes it easier to develop data collections transparently. 

But why a whole machine? So people can customize it of course!

<h2>How?</h2>

This virtual machine (VM) will include one or more open source libraries/tools for generating synthetic data.

To use it, you will just need to install Virtualbox (ideally).


